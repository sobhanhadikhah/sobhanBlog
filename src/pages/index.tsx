import Head from 'next/head';
import Cart from '~/components/elemnt/cart';
import Loading from '~/components/elemnt/loading';
import { api } from '~/utils/api';
import { ScrollArea } from '@radix-ui/themes';
export default function Home() {
  const { data, refetch, isLoading } = api.post.getAll.useQuery();

  return (
    <>
      <Head>
        <title>iBlog</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <ScrollArea type="always" scrollbars="vertical">
        <main className="grid grid-cols-12 items-center justify-center  gap-y-2  ">
          {!isLoading ? (
            data?.map((item) => <Cart refetch={refetch} key={item.id} {...item} />)
          ) : (
            <Loading />
          )}
        </main>
      </ScrollArea>
    </>
  );
}
